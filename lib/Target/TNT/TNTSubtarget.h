#ifndef LLVM_LIB_TARGET_TNT_TNTSUBTARGET_H
#define LLVM_LIB_TARGET_TNT_TNTSUBTARGET_H

#include "TNTFrameLowering.h"
#include "TNTISelLowering.h"
#include "TNTInstrInfo.h"
#include "TNTRegisterInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "TNTGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class TNTSubtarget : public TNTGenSubtargetInfo {
  virtual void anchor();
  bool ExtendedInsts;
  TNTFrameLowering FrameLowering;
  TNTInstrInfo InstrInfo;
  TNTTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  TNTSubtarget(const Triple &TT, const std::string &CPU,
                  const std::string &FS, const TargetMachine &TM);

  TNTSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const TNTInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TargetRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const TNTTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};
} // End llvm namespace

#endif  // LLVM_TARGET_TNT_SUBTARGET_H
